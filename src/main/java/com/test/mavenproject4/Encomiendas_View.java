    public void cargardatos(){
          new Thread(){
            @Override
            public void run() {
                
                Request request = new Request.Builder()
                .url("https://t-express-rest.herokuapp.com/encomiendas/")
                .header("auth-token", user.getToken())
                .build();
                
                client.newCall(request).enqueue(new Callback(){
                    @Override
                    public void onFailure(Call call, IOException ioe) {
                        
                    }

                    @Override
                    public void onResponse(Call call, Response rspns) throws IOException {
                        
                        try (ResponseBody responseBody = rspns.body()) {
                            String json = responseBody.string();
                            
                            if (!rspns.isSuccessful()) {
                        
                                JOptionPane.showMessageDialog(
                                        null,
                                        json, 
                                        "Ha ocurrido un problema",
                                        JOptionPane.ERROR_MESSAGE
                                );

                                responseBody.close();

                                return;

                            }
                            
                            List<Encomiendas> encomiendas = new Gson().fromJson(json, new TypeToken<List<Encomiendas>>() {}.getType());
                            
                            int index = 0;

                            for(Encomiendas encomienda : encomiendas){

                                jTable1.getModel().setValueAt(encomienda.getId(), index, 0);
                                jTable1.getModel().setValueAt(encomienda.getEnvia(), index, 1);
                                jTable1.getModel().setValueAt(encomienda.getRecibe(), index, 2);
                                jTable1.getModel().setValueAt(encomienda.getPrioridadText(), index, 3);
                                jTable1.getModel().setValueAt(encomienda.getFecha_entrada(), index, 4);
                                jTable1.getModel().setValueAt(encomienda.getDescripcion(), index, 6);

                                index++;
                            }
                            
                        }
                    }
                });
                
            }
        }.start();
        
    }
